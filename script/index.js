const loadlessons = () => {
  fetch("https://openapi.programming-hero.com/api/levels/all")
    .then((res) => res.json())
    .then((json) => displayLesson(json.data));
};

const removeActive = () => {
  const lessonButtons = document.querySelectorAll(".lesson-btn");
  lessonButtons.forEach((btn) => btn.classList.remove("active"));
};

const loadLevelWord = (id) => {
  const url = `https://openapi.programming-hero.com/api/level/${id}`;
  fetch(url)
    .then((res) => res.json())
    .then((data) => {
      removeActive();
      const clickBtn = document.getElementById(`lesson-btn-${id}`);
      // console.log(clickBtn);
      clickBtn.classList.add("active");
      displayLevelWord(data.data);
    });
};

const loadWordDetail = async (id) => {
  const url = `https://openapi.programming-hero.com/api/word/${id}`;
  const res = await fetch(url);
  const details = await res.json();
  displayWordDetails(details.data);
};
const displayWordDetails = (word) => {
  console.log(word);
  const detailsBox = document.getElementById("details-container");
  detailsBox.innerHTML = `
  
   <div>
            <h2 class="text-2xl font-bold">
              ${word.word} (<i class="fa-solid fa-microphone-lines"></i> : ${word.pronunciation})
            </h2>
          </div>
          <div>
            <h2 class="font-bold">Meaning</h2>
            <p class="font-bangla">${word.meaning}</p>
          </div>
          <div>
            <h2 class="font-bold">Example</h2>
            <p class="">${word.sentence}</p>
          </div>
          <div>
            <h2 class="font-bold">Synonym</h2>
            <span class="btn">Syn1</span>
            <span class="btn">Syn2</span>
            <span class="btn">Syn2</span>
          </div>
  
  `;
  document.getElementById("word_modal").showModal();
};

const displayLevelWord = (words) => {
  const wordContainer = document.getElementById("word-container");
  wordContainer.innerHTML = "";

  if (words.length == 0) {
    wordContainer.innerHTML = `
    
     <div
        class="font-bangla text-center col-span-full rounded py-10 space-y-5"
      >

      <img class="mx-auto" src="./assets/alert-error.png" alt="" />

        <p class="text-lg font-medium text-gray-400">
          এই Lesson এ এখনো কোন Vocabulary যুক্ত করা হয়নি।
        </p>

        <h2 class="font-bold text-3xl">নেক্সট Lesson এ যান</h2>
      </div>
    
    `;
    return;
  }

  words.forEach((word) => {
    console.log(word);
    const card = document.createElement("div");
    card.innerHTML = `
    
      <div
        class="bg-white rounded-xl shadow-sm text-center py-10 px-5 space-y-4"
      >
        <h2 class="font-bold text-2xl">${
          word.word ? word.word : "শব্দ পাওয়া যায়নি"
        }</h2>
        <p class="font-semibold">Meaning /Pronounciation</p>

        <div class="text-2xl font-medium font-bangla">"${
          word.meaning ? word.meaning : "অর্থ পাওয়া যায়নি"
        } / ${
      word.pronunciation ? word.pronunciation : "Pronunciation পাওয়া যায়নি"
    }"</div>
        <div class="flex justify-between items-center">
          <button onclick="loadWordDetail (${
            word.id
          })" class="btn bg-[#1A91FF10] hover:bg-[#1A91FF80] rounded-xl">
            <i class="fa-solid fa-circle-info"></i>
          </button>
          <button class="btn bg-[#1A91FF10] hover:bg-[#1A91FF80] rounded-xl">
            <i class="fa-solid fa-volume-high"></i>
          </button>
        </div>
      </div>

    `;

    wordContainer.append(card);
  });
};

const displayLesson = (lessons) => {
  //  1 get the container and empty
  const levelContainer = document.getElementById("level-container");
  levelContainer.innerHTML = "";

  //  2 get into every lesson
  for (let lesson of lessons) {
    console.log(lesson);
    // 3 create element
    const btnDiv = document.createElement("div");
    btnDiv.innerHTML = `
     <button id="lesson-btn-${lesson.level_no}" onclick="loadLevelWord(${lesson.level_no})" class="btn btn-outline btn-primary lesson-btn">
     <i class="fa-solid fa-book-open"></i> Lesson - ${lesson.level_no} </button>

    `;

    // 4 append the container
    levelContainer.append(btnDiv);
  }
};

loadlessons();
